// This function is called on the new.html page. On page load, we shoaw all the elements with the class .tab-tent, hide the export-field and add an event listener in the article-json when it is focused it highlights itself. We also add an event listener on new-form and delegates the input and textarea when they are changed we populate the preview. Finally we add an event listener on the new-form submit button. Once the form is submitted the articleView.submit function is called back.
articleView.initNewArticlePage = () => {
  $('.tab-content').show();
  $('#export-field').hide();
  $('#article-json').on('focus', function(){
    this.select();
  });

  $('#new-form').on('change', 'input, textarea', articleView.create);
  $('#new-form').on('submit', articleView.submit);
};

// COMMENT: When is this function called? What event ultimately triggers its execution?
// This method is called when there is any change on the #new-form specifically on the input or text area. This will populate the preview with the article being created.
articleView.create = () => {
  let article;
  $('#articles').empty();

  article = new Article({
    title: $('#article-title').val(),
    author: $('#article-author').val(),
    authorUrl: $('#article-author-url').val(),
    category: $('#article-category').val(),
    body: $('#article-body').val(),
    publishedOn: $('#article-published:checked').length ? new Date() : null
  });

  $('#articles').append(article.toHtml());

  $('pre code').each((i, block) => {
    hljs.highlightBlock(block);
  });

  $('#export-field').show();
  $('#article-json').val(`${JSON.stringify(article)},`);
};

// COMMENT: When is this function called? What event ultimately triggers its execution?
// This function is called when submit button is clicked by user. 
